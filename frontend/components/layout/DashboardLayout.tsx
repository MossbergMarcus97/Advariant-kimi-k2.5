const navigation = [
  { name: 'Dashboard', href: '/dashboard', icon: Icons.Home },
  { name: 'Clients', href: '/clients', icon: Icons.Users },
  { name: 'Campaigns', href: '/campaigns', icon: Icons.Target },
  { name: 'Brand Kits', href: '/brand-kits', icon: Icons.Palette },
  { name: 'Assets', href: '/assets', icon: Icons.Image },
  { name: 'Settings', href: '/settings', icon: Icons.Settings },
]

export default function DashboardLayout({ children }: { children: React.ReactNode }) {
  const [sidebarOpen, setSidebarOpen] = useState(false)
  const [user, setUser] = useState<any>(null)
  const pathname = usePathname()
  const router = useRouter()

  useEffect(() => {
    const storedUser = localStorage.getItem('advariant_user')
    if (storedUser) {
      setUser(JSON.parse(storedUser))
    }
  }, [])

  const handleLogout = () => {
    localStorage.removeItem('advariant_token')
    localStorage.removeItem('advariant_user')
    router.push('/login')
  }

  return (
    <div className="min-h-screen bg-white flex">
      {/* Mobile sidebar overlay */}
      {sidebarOpen && (
        <div 
          className="fixed inset-0 bg-black/50 z-40 lg:hidden"
          onClick={() => setSidebarOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside className={`
        fixed lg:static inset-y-0 left-0 z-50 w-64 bg-swiss-surface border-r border-swiss-border
        transform transition-transform duration-200 ease-in-out
        ${sidebarOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'}
      `}>
        {/* Logo */}
        <div className="h-16 flex items-center px-6 border-b border-swiss-border">
          <Link href="/dashboard" className="flex items-center gap-2">
            <div className="w-8 h-8 bg-lime flex items-center justify-center">
              <Icons.Logo />
            </div>
            <span className="text-lg font-bold tracking-tight">AdVariant</span>
          </Link>
        </div>

        {/* Navigation */}
        <nav className="p-4 space-y-1">
          {navigation.map((item) => {
            const isActive = pathname === item.href || pathname?.startsWith(item.href + '/')
            const Icon = item.icon
            return (
              <Link
                key={item.name}
                href={item.href}
                onClick={() => setSidebarOpen(false)}
                className={isActive ? 'sidebar-item-active' : 'sidebar-item'}
              >
                <Icon />
                <span>{item.name}</span>
              </Link>
            )
          })}
        </nav>

        {/* User section */}
        <div className="absolute bottom-0 left-0 right-0 p-4 border-t border-swiss-border">
          <div className="flex items-center gap-3 px-4 py-3">
            <div className="w-8 h-8 bg-swiss-black text-white flex items-center justify-center text-sm font-medium">
              {user?.firstName?.charAt(0) || user?.email?.charAt(0) || 'U'}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium truncate">{user?.firstName || user?.email?.split('@')[0] || 'User'}</p>
              <p className="text-xs text-swiss-muted truncate">{user?.email || ''}</p>
            </div>
          </div>
          <button onClick={handleLogout} className="sidebar-item w-full mt-2">
            <Icons.Logout />
            <span>Sign out</span>
          </button>
        </div>
      </aside>

      {/* Main content */}
      <div className="flex-1 flex flex-col min-w-0">
        {/* Top header */}
        <header className="h-16 flex items-center justify-between px-6 border-b border-swiss-border bg-white">
          <button 
            onClick={() => setSidebarOpen(true)}
            className="lg:hidden p-2 -ml-2"
          >
            <Icons.Menu />
          </button>
          
          <div className="flex items-center gap-4 ml-auto">
            <span className="tag-accent">Pro Plan</span>
          </div>
        </header>

        {/* Page content */}
        <main className="flex-1 overflow-auto">
          {children}
        </main>
      </div>
    </div>
  )
}
